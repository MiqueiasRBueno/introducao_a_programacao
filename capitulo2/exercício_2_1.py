# Exercício 2.1: Converta as expressões matemáticas para que possam ser calculadas usando o interpretador python.
# 10 + 20 x 30
# 4² ÷ 30
# (9⁴ + 2) x 6 - 1

exp1 = 10 + 20 * 30
exp2 = 4 ** 2 / 30
exp3 = (9 ** 4 + 2) * 6 - 1
print(f"O resultado da expressão 1 é {exp1}")
print(f"O resultado da expressão 2 é {exp2}")
print(f"O resultado da expressão 3 é {exp3}")
